    public class McWorker extends SwingWorker<Integer, String> {

        @Override
        protected Integer doInBackground() throws Exception {
          // Start
          publish("Start");
          setProgress(1);

          // More work was done
        int replications = Integer.parseInt(jTextFieldReplications.getText());
        double maxSimTime = Double.parseDouble(jTextFieldMaxDay.getText())*24*60*60;
        asc.setMaxSimulationTime(maxSimTime);
        asc.setNumOfReplications(replications);
        asc.init((int) jSpinnerNumberOfEmployer.getValue(),(int) jSpinnerNumberOfBus.getValue());
        asc.setMaxPocetBusov((int) jSpinnerNumberOfBus.getValue());
        
         
        initRendersVars();
        initRendersGraphs();
        
        // Variations
        int obchodniciMin = (int) jSpinnerEmplMin.getValue();
        int obchodniciMax = (int) jSpinnerEmplMax.getValue();
        int busMin = (int) jSpinnerNumBusMin.getValue();
        int busMax = (int) jSpinnerBusMax.getValue();
        
        if (obchodniciMax != 0) {
            asc.runVariationsEmployers(obchodniciMin, obchodniciMax);
        } 
        if (busMax != 0) {
            asc.runVariationsBus(busMin, busMax);
        }
        if ((busMax == 0) && (obchodniciMax == 0)){
               asc.runReplication();
        }
        


          // Complete
          System.out.println("End");
          jButtonStart.setEnabled(true);
          jButtonStop.setEnabled(false);
          jButtonPause.setEnabled(false);
          
          publish("Complete");
          setProgress(100);
          
          
          return 1;
        }

        @Override
        protected void process(List< String> chunks) {
          // Messages received from the doInBackground() (when invoking the publish() method)
        }

       
    }
    
    
   

