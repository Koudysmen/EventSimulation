    private String formatTimeSec(double seconds) {
        return String.format("%.4f", seconds);
    }
    
    private String formatTimeMin(double seconds) {
        return String.format("%.3f", (double) seconds/60 );
    }
    
    private String formatTimeHod(double seconds) {
        return String.format("%.2f", (double) seconds/60/60 );
    }
    
    private String formatTimeNormal(double seconds) {
        int sec = (int) seconds % 60;
        int min = (int) seconds / 60;
        int minModulo = (int) min % 60;
        int hour = (int) seconds / (60*60);
        int hourModulo = (int) hour % 24;
        int day = (int) seconds / (60*60*24);
        day++;
        String secModuloString = String.format("%02d", sec);
        String minModuloString = String.format("%02d", minModulo);
        String hourModuloString = String.format("%02d", hourModulo);
        return "Day: "+day+", "+hourModuloString+":"+minModuloString+":"+secModuloString;
    }
    
